          <h2>SEJARAH PERJUANGAN HIMPUNAN MAHASISWA ISLAM*</h2>
          <p>
            <strong
              >A. Latar Belakang Munculnya Pemikiran dan Berdirinya HMI</strong
            >
            <br />
            “Sesungguhnya, tahun-tahun permulaan riwayat HMI adalah hampir
            identik dengan kehidupan Lafran Pane sendiri. Karena dialah yang
            punya andil terbanyak pada mulabuka lahirnya HMI kalau tidak boleh
            kita katakan sebagai tokoh pendiri utamanya.” (Media, No.7 Th. III.
            Rajab 1376 H/ Februari 1957,h. 32). <br />
            <br />
            Dengan ungkapan ini jelaslah hubungan Lafran Pane dengan HMI tidak
            bisa dipisahkan. Latar belakang pemikiran Lafran Pane untuk
            mendirikan HMI, adalah juga identik dengan latar belakang munculnya
            pemikiran HMI. Dengan demikian memahami pemikiran Lafran Pane, akan
            senantiasa terdapat proses komunikasi dan ekspresi dengan
            lingkungannya, yaitu negara Indonesia yang berpenduduk mayoritas
            beragama Islam, dengan segala realitas dan totalitasnya. Pemikiran
            Lafran tidak bisa dipahami tanpa meletakkannya dalam suatu proses
            sejarah atau tradisi panjang yang melingkupinya. <br />
            Sesuai dengan konteksnya, latar belakang munculnya pemikiran HMI
            adalah: <br />
            <br />
            1. Penjajahan Belanda atas Indonesia dan tuntutan perang kemerdekaan
            <br />
            2. Kesenjangan dan kejumudan umat Islam dalam pengetahuan, pemahaman
            dan penghayatan serta pengamalan ajaran Islam <br />
            3. Kebutuhan akan pemahaman, penghayatan keagamaan <br />
            4. Munculnya polarisasi politik 5. Perkembangan paham dan ajaran
            komunis dikalangan Masyarakat dan Mahasiswa <br />
            6. Kedudukan Perguruan Tinggi dan dunia kemahasiswaan yang strategis
            <br />
            7. Kemajemukan bangsa Indonesia <br />
            8. Tuntutan modernisasi dan tantangan masa depan. <br />
            <br />
            Menangkap realitas historis dan berbagai persoalan dan perkembangan
            yang mengikutinya, tampilah Lafran Pane seorang mahasiswa yang sejak
            menjadi mahasiswa aktif mengamati dan memikirkan secara seksama
            perkembangan sosial, politik, dan budaya di tanah air, mengangkat
            kedelapan faktor di atas mejadi semangat spiritual. Idealisme ini
            diangkat menjadi suatu yang empiris dan pemikiran yang memiliki daya
            dukung konstruktif, guna merespon berbagai persoalan yang dihadapi
            bangsa saat itu.
          </p>
          <p>
            Setelah berulang kali mencoba mengadakan pembicaraan yang selalu
            gagal karena mendapat penentangan dari beberapa organisasi
            mahasiswa. Akhirnya, para hari Rabu Pon 1878, tanggal 14 Rabiul
            Awwal 1366 H bertepatan 5 Februari 1947 secara resmi dideklarasikan
            berdirinya Himpunan Mahasiswa Islam (HMI) oleh Lafran Pane bersama
            14 orang laninnya yaitu: Kartono Zarkasy (Ambarawa), Dahlan Husein
            (Palembang), Siti Zainah (istri Dahlan Husein, Palembang), Maisaroh
            Hilal (cucu pendiri Muhammadiyah KH. Ahmad Dahlan, Singapura),
            Soewali (Jember), Yusdi Gozali (Semarang, juga pendiri PII), M.
            Anwar (Malang), Hasan Basri (Surakarta), Marwan (Bengkulu), Tayeb
            Razak (Jakarta), Toha Mashudi (Malang), Bidron Hadi
            (Kauman-Yogyakarta), Zulkarnaen (Bengkulu), dan Mansyur.
          </p>
          <p>
            Dan dengan dua semangat atau dua tujuan pertama didirikannya HMI
            adalah semangat Keindonesiaan dan Keislaman yaitu, mempertahankan
            Negara Republik Indonesia dan mempertinggi derajat rakyat Indonesia,
            kedua menegakkan dan mengembangkan ajaran agama Islam. Dua tujuan
            ini selalu menyemai dalam semangat kader HMI baik dalam gagasan
            maupun tindakan.
          </p>
          <p>
            <strong>*Sekilas sosok Lafran Pane</strong> <br />
            Berdasarkan penelusuran dan penelitian sejarah, maka Kongres XI HMI
            tahun 1974 di Bogor menetapkan Lafran Pane sebagai pemrakarsa
            berdirinya HMI, dan disebut sebagai pendiri HMI. <br />
            <br />
            Lafran Pane adalah anak keenam dari SutanPangurabaan Pane, lahir di
            Padang Sidempuan, 5 Februari 1922, pendidikan Lafran Pane tidak
            berjalan normal dan lurus. Lafran Pane terinspirasi dari gerakan
            kelompok pelajar Islam di era Hindia Belanda yaitu Jong Islamieten
            Bond (JIB) dan Student Islamic Studenten (SIS). Lafran mengalami
            perubahan kejiwaan yang radikal sehingga mendorong dirinya untuk
            mencari hakikat hidup sebenarnya. Desember 1945 Lafran Pane pindah
            ke Yogyakarta, karena Sekolah Tinggi Islam (STI) tempat ia menimba
            ilmu pindah dari Jakarta ke Yogyakarta. Pendidikan agama Islam yang
            lebih intensif ia peroleh dari dosen-dosen STI, mengubur masa lampau
            yang kelam. <br />
            <br />
            Bagi Lafran Pane, Islam merupakan satu-satunya pedoman hidup yang
            sempurna, karena Islam menjadikan manusia sejahtera dan selamat di
            dunia dan akhirat. Pada tahun 1948, Lafran Pane pindah studi ke
            Akademi Ilmu Politik (AIP). Saat Balai Perguruan Tinggi Gadjah Mada
            dan Fakultas Kedokteran di Klaten, serta AIP Yogyakarta dinegerikan
            pada tanggal 19 Desember 1949 menjadi Universitas Gadjah Mada (UGM),
            secara otomatis Lafran Pane termasuk mahasiswa pertama UGM. Setelah
            bergabung menjadi UGM, AIP berubah menjadi Fakultas Hukum Ekonomi
            Sosial Politik, dan Lafran Pane menjadi sarjana pertama dalam ilmu
            politik dari fakultas tersebut pada tanggal 26 Januari 1953.
          </p>
          <p>
            <strong>B. Fase-fase Perjuangan HMI</strong> <br />
            Dalam perjalanan HMI selama setengah abad lebih, telah menjalani 11
            fase.
          </p>
          <p>
            <strong
              >1. Fase Konsolidasi Spiritual dan Proses Berdirinya HMI (tahun
              1946)</strong
            >
            <br />
            Bermula dari latar belakang munculnya pemikiran dan berdirinya HMI
            serta kondisi obyektif yang mendorongnya, maka rintisan untuk
            mendirikan HMI muncul di bulan November 1946. Permasalahan yang
            dapat diangkat dari latar belakang berdirinya HMI, merupakan suatu
            kenyataan yang harus diantisipasi dan dijawab secara cepat dan
            konkrit dan menunjukkan apa sebenarnya Islam itu. Maka pembaharuan
            pemikiran di kalangan umat Islam bangsa Indonesia suatu keniscayaan.
          </p>
          <p>
            <strong
              >2. Fase Berdiri dan Pengokohan (5 Februari – 30 November
              1947)</strong
            >
            <br />
            Selama lebih kurang 9 bulan, reaksi-reaksi terhadap HMI barulah
            berakhir. Masa 9 bulan itu dipergunakan untuk menjawab berbagai
            reaksi dan tantangan silih berganti, yang semuanya itu untuk
            mengokohkan eksistensi HMI, sehingga dapat berdiri tegar dan kokoh.
            Maka diadakanlah berbagai aktivitas untuk popularisasi organisasi
            dengan mengadakan ceramah-ceramah ilmiah, rekreasi malam-malam
            kesenian. <br />
            <br />
            1. Di bidang organisasi, HMI mulai mendirikan cabang- cabang baru
            seperti Klaten, Solo dan Yogyakarta. Pengurus HMI bentukan 5
            Februari 1947 otomatis menjadi Pengurus Besar (PB) HMI pertama dan
            merangkap menjadi Pengurus HMI Cabang Yogyakarta I. Hari Rabu Pon
            1878, tanggal 14 RA 1366 / 5 Februari 1947, menetapkan berdirinya
            organisasi Himpunan Mahasiswa Islam, disingkat HMI <br />
            2. Mengesahkan Anggara Dasar HMI. Adapun Anggaran Rumah Tangga
            dibuat kemudian. <br />
            3. Membentuk Pengurus HMI : <br />
            Ketua : Lafran Pane (Prof. Drs. Alm.) <br />
            <br />
            Wakil Ketua : Asmin Nasution (Drs.) <br />
            <br />
            Penulis I : Anton Timur Jailani (Prof. H. – MA) <br />
            <br />
            Penulis II : Karnoto Zarkasyi (Kapten AD – BA) <br />
            <br />
            Bendahara I : Dahlan Husein <br />
            <br />
            Bendahara II : Maisaroh Hilal <br />
            <br />
            Anggota : Suwali, Yusdi Ghozali (SH),Mansyur <br />
            <br />
            Ada kesan bahwa keanggotaan HMI hanya untuk mahasiswa STI. Untuk
            menghilangkan anggapan yang keliru itu, tanggal 22 agustus 1947, PB
            HMI diresuffle. Ketua Lafran Pane digantikan oleh H.M. Mintaredja
            dari Fakultas Hukum BPT GM, sedang Lafran Pane menjadi wakil ketua
            merangkap Ketua HMI Cabang Yogyakarta. Sejak itu mahasiswa BPT GM,
            STT mulai masuk dan berbondong-bondong menjadi anggota HMI. Di
            Yogyakarta tanggal 30 November 1947 diadakan Kongres I HMI.
          </p>
          <p>
            <strong
              >3. Fase Perjuangan Bersenjata dan Perang Kemerdekaan, dan
              Menghadapi Pengkhianatan dan Pemberontakan PKI (1947-
              1949)</strong
            >
            <br />
            Seiring dengan tujuan HMI yang digariskan sejak awal berdirinya,
            maka konsekuensinya dalam masa perang kemerdekaan, HMI terjun ke
            gelanggang medan pertempuran melawan Belanda. Tepat saat Agresi
            Militer Belanda I 21 April 1947 sekretariat HMI di JL.Setyodingrat
            terkena hantaman senjata Belanda oleh karena itu anggota HMI
            membantu pemerintah baik langsung memegang senjata bedil dan bambu
            runcing sebagai staf penerangan, penghubung, dll. <br />
            Untuk menghadapi pemberontakan Madiun 18 September 1948, Ketua
            PMI/Wakil Ketua PB HMI Ahmad Tirtosudiro membentuk Corps Mahasiswa
            (CM), dengan Komandan Hartono, Wakil Komandan Ahmad Tirtosudiro,
            ikut membantu pemerintah menumpas pemberontakan PKI di Madiun,
            dengan mengerahkan anggota CM ke gunung- gunung, memperkuat aparat
            pemerintah. Sejak itulah dendam PKI terhadap HMI tertanam dan terus
            berlanjut sampai puncaknya pada tahun 1964-1965 yaitu gerakan
            penggayangan terhadap HMI menjelang meletusnya Gestapu/PKI 1965.
            <br />
            Pada fase ini berlangsung peringatan Dies Natalies pertama HMI di
            Bangsal Kepatihan tanggal 6 Februari 1948, Panglima Angkatan Perang
            Republik Indonesia Jenderal Sudirman memberi sambutan pada
            peringatan tersebut atas nama Pemerintah RI. Jenderal sudirman
            selain mengartikan HMI sebagai Himpunan Mahasiswa Islam, HMI juga
            diartikan sebagai Harapan Masyakat Indonesia. Karena mayoritas
            bangsa Indonesia beragama Islam, HMI juga diartikan sebagai Harapan
            Masyarakat Islam Indonesia. <br />
            Pada fase ini juga berlangsung Kongres Muslim Indonesia II di
            Yogyakarta tanggal 20 sampai dengan 25 Desember 1949. Kongres itu
            dihadiri oleh 185 organisasi, alim ulama dan intelegensia seluruh
            Indonesia.Di antara tujuh dari keputusannya di bidang organisasi
            salah satu keputusannya adalah memutuskan bahwa: Hanya satu
            organisasi mahasiswa Islam, yaitu Himpunan Mahasiswa Islam (HMI)
            yang bercabang di tiap-tiap kota yang ada sekolah tinggi.
          </p>
          <p>
            <strong
              >4. FasePembinaandanPengembanganOrganisasi(1950-1963)</strong
            >
            <br />
            Selama anggota HMI banyak yang terjun ke gelanggang medan
            pertempuran membantu pemerintah mengusir penjajah, selama itu pula
            pembinaan organisasi HMI terabaikan. Namun hal itu dilaksanakan
            dengan sadar, karena ini semua untuk merealisir tujuan HMI sendiri,
            serta dwitugasnya, yakni tugas agamanya dan tugas bangsanya. Maka
            dengan adanya pengakuan kedaulatan rakyat tanggal 27 Desember 1949,
            mahasiswa yang berminat melanjutkan kuliahnya bermunculan di
            Yogyakarta. <br />
            Sejak tahun 1950, dilaksanakanlah usaha-usaha konsolidasi organisasi
            sebagai masalah besar sepanjang masa. Bulan Juli 1951 PB HMI
            dipindahkan dari Yogyakarta ke Jakarta. Diantara usaha-usaha yang
            dilaksanakan selama 13 tahun itu antara lain: <br />
            <br />
            a. Pembentukan cabang-cabang baru,<br />
            b. Menerbitkanmajalahsejak1Agustus1954,sebelumnyaterbitCriterium,
            <br />
            Cerdas dan tahun 1959 menerbitkan majalah Media,<br />
            c. 7 kali kongres,<br />
            d. PengesahanatributHMIsepertilambing,bendera,muts,hymneHMI,<br />
            e. Merumuskan tafsir azas HMI,<br />
            f. Pengesahan kepribadian HMI,<br />
            g. PembentukanBadanKoordinasi(Badko),<br />
            h. MenentukanmetodeTrainingHMI,<br />
            i. Pembentukan lembaga-lembaga HMI di Bidang ekstern,<br />
            j. Pendayagunaan PPMI,<br />
            k. Menghadapi Pemilu 1955,<br />
            l. Penegasan Independensi HMI,<br />
            m. Mendesak pemerintah supaya mengeluarkan Undang-undang Perguruan
            Tinggi, tuntutan agar pendidikan agama sejak dari SR sampai
            Perguruan Tinggi,<br />
            n. Mengeluarkankonsepperanagamadalampembangunan,danlain-lain. <br />
            Selain masalah internal, muncul pula persoalan ekstern yang sangat
            menonjol. Justru karena keberhasilan HMI melaksanakan konsolidasi
            organisasi ada golongan yang iri dan tidak senang kepada HMI yaitu
            PKI. <br />
            Tidak dibubarkan dan dilarangnya PKI akibatnya pemberontakan PKI di
            Madiun tahun 1948, PKI otomatis mempunyai kesempatan untuk bangkit
            kembali. Tanggal 21 Februari tahun 1957, Presiden Soekarno
            mengumumkan konsepsinya supaya kabinet berkaki empat dengan unsur
            PNI, Masyumi, NU dan PKI (sebagai empat besar pemenang pemilu 1955).
            Berikutnya di Moskow tanggal 19 November 1957 dicetuskanlah
            Manifesto Moscow, yaitu satu program untuk mengkomuniskan Indonesia.
            Akibat itu semua, PKI tampil sebagai partai pemerintah. Masyumi,
            akibat penentangan terhadap kebijakan politik Presiden Soekarno,
            dengan Manipol Usdeknya, dengan Keputusan Presiden nomor 200:
            tanggal 17 Agustus tahun 1960 Masyumi dipaksa bubar. Untuk
            menghadapi perkembangan politik, Kongres V HMI di Medan tanggal
            24-31 Desember 1957 mengeluarkan dua sikap anatar lain: <br /><br />
            a. Haram hukumnya menganut ajaran dan paham komunis karena <br />
            bertentangan dengan Islam, <br />b. MenuntutIslamsebagaidasarNegara.
          </p>
          <p>
            <strong>5. FaseTantanganI(1964-1965)</strong> <br />
            Dendam PKI terhadap HMI yang tertanam karena keikutsertaan HMI dalam
            menumpas pemberontakan PKI di Madiun tahun 1948, menempatkan HMI
            sebagai organisasi yang harus bubar, karena dianggap sebagai
            penghalang bagi tercapainya tujuan PKI. Untuk itu dilaksanakanlah
            berbagai usaha untuk membubarkan HMI. <br />
            Sesuai hasil Kongres II Consetrasi Gerakan Mahasiswa Indonesia
            (CGMI) organisasi mahasiswa underbow PKI di Salatiga, Juni 1961,
            untuk melekuidasi HMI. PKI, CGMI dan organisasi lainnya yang
            se-ideologi mulai melakukan gerakan secara terbuka untuk membubarkan
            HMI. Gerakan pembubaran HMI disokong seluruh simpatisan dari tiga
            partai besar yaitu Partai Komunis Indonesia (PKI), Partai Indonesia
            (PARTINDO) dan Partai nasional Indonesia (PNI) dan seluruh underbow
            ketiga partai tersebut yang semuanya berjumlah 42 partai. Untuk
            membubarkan HMI sekitar Maret 1965, dibentuklah Panitia Aksi
            Pembubaran HMI di Jakarta yang terdiri dari CGMI, GMNI, IPPI,
            GRMINDO, GMD, MMI, Pemuda Marhaenis, Pemuda Rakyat, Pemuda
            Indonesia, PPI, dan APPI. <br />
            Menjawab tantangan ini, Generasi Muda Islam(GEMUIS) yang terbentuk
            tahun 1964 membentuk Panitia Solidaritas Pembelaan HMI, yang terdiri
            dari unsur- unsur pemuda, pelajar, mahasiswa Islam seluruh
            Indonesia. Bagi umat Islam HMI merupakan taruhan terakhir yang harus
            dipertahankan setelah sebelumya Masyumi dibubarkan. Kalau HMI sempat
            bubar, maka satu-persatu dari organisasi Islam akan terkena sapu
            pembubaran. <br />
            Namun gerakan pembubaran HMI ini gagal justru dipuncak usaha- usaha
            pembubaran tersebut. Dalam acara penutupan Kongres CGMI tanggal 29
            September 1965 di Istora Senayan. Meski PKI terus melakukan
            provokasi kepada Presiden Soekarno, seperti diungkapkan DN. Aidit,
            ‖kalau anggota CGMI tidak bisa membubarkan HMI, anggota CGMI yang
            laki-laki lebih baik pakai kain sarung saja..... kalau semua front
            sudah minta, Presiden akan membubarkan HMI.‖ Namun ternyata HMI
            tidak dibubarkan, bahkan dengan tegas Presiden Soekarno
            mengungkapkan dalam pidatonya: <br />
            “Pemerintah mempunyai kebijakan untuk memberikan kesempatan
            seluas-luasnya kepada kehidupan organisasi mahasiswa yang
            revolusioner. Tapi kalau organisasi mahasiswa yang menyeleweng itu
            mejadi kontra revolusi umpamanya HMI, aku sendiri yang akan
            membubarkannya. Demikian pula kalau CGMI menyeleweng menjadi kontra
            revolusi juga akan kububarkan.” <br />
            Antara lain karena gagal membubarkan HMI, maka PKI sudah siap main
            kayu, main kekerasan. PKI takut didahului umat Islam untuk merebut
            kekuasaan dari pemerintahan yang sah, maka meletuslah Pemberontakan
            G30S/PKI 1965. <br />
          </p>
          <p>
            <strong>
              6. Fase Kebangkitan HMI sebagai Pejuang Orde Baru dan Pelopor
              Kebangkitan Angkatan 66 (1966-1968)</strong
            >
            <br />
            a. Tanggal 1 Oktober adalah tugu pemisah antara Orde Lama dan Orde
            Baru <br />
            b. Apa yang disinyalir PKI, seandainya PKI gagal membubarkan HMI,
            HMI akan tampil kedua kalinya menumpas pemberontakan PKI,
            benar-benar terjadi. <br />
            c. Wakil Ketua PB HMI Mar‘ie Muhammad tanggal 25 Oktober 1965
            mengambil inisiatif mendirikan Kesatuan Aksi Mahasiswa Indonesia
            (KAMI), sebagaimana yang dilakukan oleh Wakil Ketua PB HMI Ahmad
            Tirtosudiro membentuk Corps Mahasiswa (CM) untuk menghadapi
            pemberontakan PKI di Madiun. <br />
            d. Tritura 10 Januari 1966: ―bubarkan PKI, reatol kabinet, dan
            turunkan harga <br />
            e. Surat Perintah Sebelas Maret 1966 <br />
            f. Dibubarkan dan dilarangnya PKI tanggal 12 Maret 1966 <br />
            g. Kabinet Ampera terbentuk, HMI diajak hearing pembentukan kabinet,
            dan alumni HMI masuk dalam kabinet. <br />
          </p>
          <p>
            <strong
              >7. Fase Partisipasi HMI dalam Pembangunan(1969-sekarang)</strong
            >
            <br />
            Setelah Orde Baru mantap dan Pancasila serta UUD 1945 sudah
            dilaksanakan secara murni dan konsekuen, maka sejak tanggal 1 April
            1969 dimulailah rencana pembangunan lima tahun dan sudah
            menyelesaikan pembangunan 25 tahun pertama, kemudian menyusul
            pembangunan 25 tahun kedua. Pembangunan Indonesia menuju masyarakat
            adil dan makmur bukanlah pekerjaan mudah, tetapi sebaliknya
            merupakan pembangunan raksasa sebagai usaha kemanusiaan yang tidak
            habis- habisnya. Partisipasi segenap warga negara sangat dibutuhkan.
            HMI pun sesuai dengan lima aspek pemikirannya, telah memberikan
            sumbangan dan partisipasinya dalam pembangunan: (a) partisipasi
            dalam pembentukan suasana, situasi dan iklim yang memungkinkan
            dilaksanakannya pembangunan, (b) partisipasi dalam pemberian
            konsep-konsep dalam berbagai aspek pemikiran; (partisipasi dalam
            bentuk langsung dari pembangunan).
          </p>
          <p>
            <strong
              >8. Fase Pergolakandan Pembaharuan Pemikiran (1970-1998)</strong
            >
            <br />
            Selama kurun waktu Orde Lama (1959-1965) kebebasan mengeluarkan
            pendapat baik yang bersifat akademis terlebih-lebih politik
            terkekang dengan ketat. Suasana itu berubah tatkala Orde Baru
            muncul, walaupun kebebasan hakiki belum diperoleh sebagaimana
            mestinya. Sama halnya di penghujung pemerintahan Soeharto dianggap
            sebagai suatu perbedaan yang tidak pada tempatnya. Namun walaupun
            demikian, kebebasan datang, kondisi terbatas dapat dimanfaatkan,
            baik yang berkaitan dengan agama, akademik dan politik. Kejumudan
            dan suasana tertekan pada masa Orde Lama mulai cair terutama dalam
            pembaharuan pemikiran Islam yang dipandang sebagai suatu keharusan,
            sebagai jawaban terhadap berbagai masalah untuk memenuhi kebutuhan
            kontemporer. Hal seperti itu muncul di kalangan HMI dan mencapai
            puncaknya tahun 1970. Tatkala Nurcholis Madjid menyampaikan ide
            pembaharuannya dengan topik Keharusan Pembaharuan Pemikiran dalam
            Islam dan Masalah Integrasi Umat. Sikap itu diambil, karena apabila
            kondisi ini dibiarkan mengakibatkan persoalan-persoalan umat yang
            terbelenggu selama ini, tidak akan memperoleh jawaban yang efektif.
            <br />
            Sebagai konsekuensinya muncul pergolakan pemikiran dalam tubuh HMI
            yang dalam berbagai substansi permasalahan timbul perbedaan
            pendapat, penafsiran dan interpretasi. Hal itu tercuat dalam bentuk
            seperti persoalan negara Islam, Islam Kaffah, sampai kepada
            penyesuaian dasar HMI dari Islam menjadi Pancasila. Sejak
            diberlakukannya Undang-Undang Nomor: 8/1985 yang mengharuskan bahwa
            semua partai dan organisasi harus berdasarkan Pancasila. Kongres
            ke-16 HMI di Padang tahun 1986, HMI menyesuaikan diri dengan
            mengubah asas Islam dengan Pancasila. Akibat penyesuaian ini
            beberapa orang anggota HMI membentuk MPO, akibatnya HMI pecah
            menjadi dua yaitu HMI DIPO dan HMI MPO.
          </p>
          <p>
            <strong>9. Fase Reformasi(1998-2000)</strong> <br />
            Apabila dicermati dengan seksama secara historis HMI sudah mulai
            melaksanakan gerakan reformasi dengan menyampaikan beberapa
            pandangan yang berbeda serta kritik maupun evaluasi secara langsung
            terhadap pemerintahan Orde Baru di bawah kepemimpinan Presiden
            Soeharto pada tahun 1995. Sesuai dengan kebijakan PB HMI, bahwa HMI
            tidak akan melakukan tindakan-tindakan inkonstitusional dan
            konfrontasi terhadap Pemerintah. HMI melakukan dan menyampaikan
            kritik secara langsung yang bersifat konstruktif. <br />
            Koreksi dan kritik yang dimaksud, pertama, disampaikan M. Yahya
            Zaini Ketua Umum PB HMI Periode 1992-1995 ketika memberikan sambutan
            pada pembukaan Kongres ke-20 HMI di Istana Negara Jakarta tanggal 21
            Januari 1995. Koreksi itu antara lain, bahwa menurut penilaian HMI,
            pembangunan ekonomi kurang diikuti dengan pembangunan politik. Masih
            dirasakan tingkat perubahan di tingkat politik tidak sebanding
            dengan apa yang terjadi di bidang ekonomi. Dalam pembangunan politik
            institusi-institusi politik atau badan-badan demokrasi belum
            maksimal memainkan fungsi dan peranannya. Akibatnya aspirasi
            masyarakat masih sering tersumbat. Kondisi inilah yang menutut kita,
            pemerintah dan masyarakat untuk terus menggelindingkan proses
            demokratisasi dengan bingkai Pancasila tetapi ini harus diikuti
            dengan pemberdayaan masyarakat. Dalam suasana demikian, proses
            saling kontrol akan terbangun. Selain itu HMI melihat masih banyak
            distorsi dalam proses pembangunan. Gejala penyalahgunaan kekuasaan,
            kesewenang-wenangan, praktek kolusi, korupsi, dan nepotisme adalah
            cerminan tidak berfungsinya sistem nilai yang menjadi kontrol dan
            landasan etika dan bekerjannya suatu sistem. <br />
            Suara reformasi berikutnya dengan fokus yang lebih tajam, lugas
            dihadapan Presiden Soeharto tatkala menghadiri dan memberikan
            sambutan pada peringatan Ulang Tahun Emas 50 tahun HMI di Jakarta
            tanggal 20 Maret 1997 (satu tahun sebelum reformasi), dimana Taufik
            Hidayat Ketua Umum PB HMI 1995-1997 menegaskan; sekaligus sebagai
            jawaban atas kritik-kritik yang memandang HMI terlalu dekat dengan
            kekuasaan. Bagi HMI, kekuasaan atau politik bukanlah wilayah yang
            haram, politik justru mulia, apabila dijalankan di atas etika dan
            bertujuan untuk menegakkan nilai- nilai kebenaran dan keadilan.
            Lantaran itu, HMI akan mendukung kekuasaan pemerintah yang
            sungguh-sungguh dalam memperjuangkan kebenaran dan keadilan.
            Sebaliknya, HMI akan tampil ke depan menentang kekuasaan yang korup
            dan menyeleweng. Ini telah dibuktikan ketika HMI terlibat aktif
            dalam merintis dan menegakkan Orde Baru. Demikian juga pada saat
            sekarang ini dan masa-masa mendatang. Kritik- kritik ini tidak boleh
            mengurangi rasa percaya diri HMI untuk tetap melaksanakan amar
            ma‟ruf dan nahi munkar. <br />
            Pemikiran dan reformasi selanjutnya disampaikan Ketua Umum PB HMI
            1997-1999 Anas Urbaningrum pada waktu peringatan Dies Natalis HMI
            ke-51 di Graha Insan Cita Depok tanggal 22 Februari 1998, dengan
            judul Urgensi Reformasi Bagi Pembangunan Bangsa yang Bermartabat.
            Pidato itu disampaikan 3 bulan sebelum lengsernya Presiden Soeharto
            21 Mei 1998. Suara dan tuntutan reformasi telah dikumandangkan pula
            dalam berbagai aspek, yang disampaikan Anas Urbaningrum pada
            Peringatan Dies Natalis ke- 52 di Auditorium Sapta Pesona Departemen
            Pariwisata Seni dan Budaya Jakarta 5 Februari 1999, dengan judul
            Dari HMI untuk Kebersamaan Bangsa Menuju Indonesia Baru. Tuntutan
            reformasi juga disampaikan Ketua Umum PB HMI M. Fahruddin pada
            Peringatan Dies Natalis ke-53 HMI di Taman Ismail Marzuki Jakarta, 5
            Februari 2000 dengan judul Merajut Kekuasaan Oposisi Membangun
            Demokrasi Membangun Peradaban Baru Indonesia. <br />
          </p>
          <p>
            <strong>10.Fase Tantangan II (2000-sekarang)</strong> <br />
            Fase tantangan ke-2 ini muncul justru setelah Orde Reformasi
            berjalan dua tahun. Semestinya berdasarkan landasan-landasan atau
            sikap- sikap yang telah diambil PB HMI memasuki era reformasi
            semestinya HMI mengalami perkembangan yang signifikan menjawab
            berbagai tantangan sesuai dengan perannya sebagai organisasi
            perjuangan, yang harus tampil sebagai pengambil inisiatif dalam
            memajukan kehidupan masyarakat, berbangsa, dan bernegara. Akan
            tetapi justru sebaliknya HMI secara umum mengalami kemunduran, yang
            secara intensif disinyalir Agussalim Sitompul dalam bukunya 44
            Indikator Kemunduran HMI. <br />
            Jika pada fase tantangan I (1964-1965) HMI dihadapkan kepada
            tantangan eksternal yaitu menghadapi PKI, pada fase tantangan II ini
            HMI dihadapkan sekaligus pada dua tantangan besar secara internal
            dan eksternal sekaligus. <br />
            Pertama, tantangan internal, kajian tentang HMI saat ini
            menunjukkan, bahwa dalam kehidupan sekarang dan mendatang, HMI
            ditantang: <br />
            a. Masalah eksistensi dan keberadaan HMI, seperti menurunnya jumlah
            mahasiswa baru masuk HMI, tidak terdapatnya HMI di berbagai
            perguruan tinggi, institut, fakultas, akademi, program studi,
            sebagai basis HMI. <br />
            b. Masalahrelevansipemikiran-pemikiranHMI,untukmelakukanperbaikan
            dan perubahan yang mendasar terhadap berbagai masalah yang muncul
            yang dihadapi bangsa Indonesia. <br />
            c. Masalah peran HMI sebagai organisasi perjuangan yang sanggup
            tampil dalam barisan terdepan sebagai avant grade, kader pelopor
            bangsa dalam mengambil inisiatif untuk melakukan berbagai perubahan
            yang sangat dibutuhkan masyarakat. <br />
            d. Masalah efektifitas HMI untuk memecahkan masalah yang dihadapi
            bangsa, karena banyak organisasi yang sejenis maupun yang lain yang
            dapat tampil lebih efektif dan dapat mengambil inisiatif terdepan
            untuk memberi solusi terhadap problem yang dihadapi bangsa
            Indonesia. <br />
            Sebagai jawabannya, menuntut perpecahan yang bersifat teoritis dan
            praktis, akan tetapi semuanya bersifat konseptual, integratif,
            inklusif. Sebab pendekatan yang tidak konseptual, parsial dan
            ekslusif tidak akan melahirkan jawaban yang efektif. Untuk itu
            dibutuhkan ide dan pemikiran dari anggota aktifitas kader, dan
            pengurus HMI di seluruh jenjang organisasi. <br />
            Kedua, tantangan eksternal, berbagai tantangan eksternal juga
            dihadapkan kepada HMI yang tidak skala besar dan rumitnya dari
            tantangan internal, antara lain: <br />
            a. Tantangan menghadapi perubahan zaman yang jauh berbeda dari abad
            ke-20 dan yang muncul pada abad ke-21 ini. <br />
            b. Tantangan terhadap peralihan generasi yang hidup dalam zaman dan
            situasi yang berada dalam berbagai aspek kehidupan khususnya yang
            dijalani generasi muda bangsa. <br />
            c. Tantangan untuk mempersiapkan kader-kader dan alumni HMI, yang
            akan menggantikan alumni-alumni HMI yang saat ini menduduki berbagai
            posisi strategis dalam kehidupan bermasyarakat, berbangsa, dan
            bernegara. Karena regenerasi atau pergantian pejabat-pejabat, suka
            tidak suka, mau tidak mau pasti terus berlangsung. <br />
            d. Tantanganmenghadapibahayaabadikomunis. <br />
            e. Tantangan menghadapi golongan lain, yang mempunyai misi lain dari
            umat Islam dan bangsa Indonesia. <br />
            f. Tantangan tentang adanya kerawanan aqidah. <br />
            g. Tantanganmenghadapikemajuanilmupengetahuandanteknologi,yang terus
            berkembang tanpa henti. <br />
            h. Tantangan menghadapi perubahan dan pembaharuan di segala aspek
            kehidupan manusia yang terus berlangsung sesuai dengan semangat
            kemajuan ilmu pengetahuan dan teknologi yang sangat kompetitif.
            <br />
            i. Tantangan menghadapi masa depan yang belum dapat diketahui bentuk
            dan coraknya. <br />
            j. Kondisi umat Islam di Indonesia yang dalam kondisi belum bersatu.
            <br />
            k. Kondisi dan keadaan Perguruan Tinggi serta dunia kemahasiswaan,
            kepemudaan, yang penuh dengan berbagai persoalan dan problematika
            yang sangat kompleks. <br />
            Pada fase tantangan II ini, nampaknya HMI semakin memudar dan mundur
            yang telah berlangsung 25 tahun sejak tahun 1980-2005.HMI tidak
            mampu bangkit secara signifikan, bahkan dalam dua periode terakhir
            PB HMI mengalami perpecahan.Karena itu, menghadapi tantangan
            tersebut, HMI dengan segenap aparatnya harus mampu menghadapinya
            dengan penuh semangat dan militansi yang tinggi. Apakah HMI mampu
            menghadapi tantangan itu, sangat ditentukan oleh pemegang kendali
            organisasi sejak dari PB HMI, Pengurus Badko, Cabang, Korkom,
            Komisariat, Lembaga- Lembaga Kekaryaan, serta segenap anggota HMI,
            maupun alumninya yang tergabung dalam KAHMI sebagai penerus,
            pelanjut serta penyempurna mission sacre HMI. Peralihan zaman,
            peralihan generasi, saat ini menentukan bagi eksistensi HMI di masa
            mendatang. <br />
          </p>
          <p>
            <strong>11.Fase Kebangkitan Kembali (2006-sekarang)</strong> <br />
            Gelombang kritik terhadap HMI tentang kemundurannya, telah
            menghasilkan dua umpan balik.Pertama, telah muncul kesadaran
            individual dan kolektif di kalangan anggota, aktivis, kader, bahkan
            alumni HMI serta pengurus sejak dari Komisariat sampai PB HMI, bahwa
            HMI sedang mengalami kemunduran.Kedua, selanjutnya dari kesadaran
            itu muncul pula kesadaran baru, baik secara individual dan kolektif
            di kalangan anggota, aktivis, kader, alumni, dan pengurus bahwa
            dalam tubuh HMI mutlak dilakukan perubahan dan pembaharuan, supaya
            dapat bangkit kembali seperti masa jaya-jayanya dulu. <br />
            Sampai sejauh mana kebenaran dan bukti adanya indikator-indikator
            kebangkitan kembali HMI, sejarahlah yang akan menentukan kelak. Kita
            semua berharap dengan penuh optimistis sesuai dengan ajaran Islam
            supaya manusia bersikap optimis, agar HMI dapat mengakhiri masa
            kemundurannya dan memasuki masa kebangkitannya secara meyakinkan.
            <br />
            Di tangan generasi sekaranglah sebagai generasi penerus, pelanjut,
            dan penyempurna perjuangan HMI. Yakin Usaha Sampai!
          </p>
